package com.itheima.note;

/**
 * w这是文档注释，可以写多句话。
 * 文档注释可以自动提取到文档说明书中
 * 注释不影响程序执行
 */
public class NoteDemo {
    public static void main(String[] args) {
        // 这是一个打印注释案例
        System.out.println("我开始学习java程序的注释");
        /*
        床前明月光，
        疑是地上霜。
        举头望明月，
        低头思故乡。
         */
        System.out.println("结束标签。");
    }
}
