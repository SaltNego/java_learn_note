package com.itheima.variable;

public class VariableDemo1 {
    // 学会使用变量
    public static void main(String[] args) {
        // 数据类型 变量名称 = 初始值
        double money = 666.6 ;
        System.out.println(money);
        // 收红包
        money = money + 4.44;
        System.out.println(money);

        System.out.println("-----------------------------");

        int age = 25;
        age = age +5;
        age += 6;
        System.out.println(age);

    }
}
